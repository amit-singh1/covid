            <div id="demo2" class="collapse">
                located in:NORTH GOA<br>
                The fort was built in 1612 to watch and guard against the Dutch and the Marathas. It was a reference point for the vessels coming from Europe. This old Portuguese fortification stands on the beach south of Candolim, at the shore of the Mandovi River. Earlier, it was tasked with the defense of shipping and the Bardez sub district in the vicinity.
A freshwater spring within the fortification provided water supply to the ships that used to stop by. This is how the fort got its name Aguada, meaning Water. Crews of passing ships would often visit to replenish their fresh water stores. A four-storey Portuguese lighthouse is placed on the shore, erected in 1864 and the oldest of its kind in the whole of Asia. Constructed in 1612, it was once the grandstand of 79 cannons. It has the capacity of storing 2,376,000 gallons of water, one of the biggest freshwater reservoirs of Asia.<br>
